#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	src_len;

	i = 0;
	src_len = (size_t)ft_strlen(src);
	if (dstsize > 0)
	{
		while (i < dstsize - 1)
		{
			if (src[i] != '\0')
				dst[i] = src[i];
			else
				break ;
			i++;
		}
		dst[i] = '\0';
	}
	return (src_len);
}
